package k2hdkc

// Result is the interface of getting a result of a Command.
type Result interface {
	Bool() bool
	String() string
}

// Command is the interface of commands which are executed by Sessions.
type Command interface {
	Execute(s *Session) (bool, error)
	String() string
}

